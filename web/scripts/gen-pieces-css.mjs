// Regenerates web/src/pieces.css from the piece sets in web/public/pieces.
// Run with: node scripts/gen-pieces-css.mjs (from the web/ directory).
//
// Pieces are Lichess's open-source SVG sets (GPL); switching themes is a single
// `.theme-<set>` class change on the chessground wrapper.
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));

/** Keep in sync with PIECE_THEMES in src/lib/settings.tsx. */
const SETS = ['cburnett', 'merida', 'alpha', 'california', 'gioco'];
const COLORS = { w: 'white', b: 'black' };
const ROLES = { P: 'pawn', N: 'knight', B: 'bishop', R: 'rook', Q: 'queen', K: 'king' };

let out =
  '/*\n' +
  ' * Chess piece themes — Lichess open-source piece sets, served from\n' +
  ' * /public/pieces (see public/pieces/README.md for sources & licenses).\n' +
  ' * GENERATED by scripts/gen-pieces-css.mjs; edit the generator, not this\n' +
  ' * file. Each theme is scoped by a `.theme-<set>` class on the chessground\n' +
  ' * wrapper so switching themes is a single class change.\n' +
  ' */\n';

for (const set of SETS) {
  out += `\n/* ${set} */\n`;
  for (const [c, color] of Object.entries(COLORS)) {
    for (const [r, role] of Object.entries(ROLES)) {
      out += `.cg-wrap.theme-${set} piece.${role}.${color}{background-image:url("/pieces/${set}/${c}${r}.svg")}\n`;
    }
  }
}

const dest = resolve(__dirname, '../src/pieces.css');
writeFileSync(dest, out);
console.log(`Wrote ${dest}`);
